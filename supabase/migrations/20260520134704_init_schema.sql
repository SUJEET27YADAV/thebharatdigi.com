-- Create RLS policies
-- Only admins can view other admins
CREATE POLICY "Admins can view all users" ON admin_users
  FOR SELECT USING (auth.role() = 'authenticated');

-- Only super admins can update users
CREATE POLICY "Super admins can update users" ON admin_users
  FOR UPDATE USING (
    EXISTS (
      SELECT 1 FROM admin_users
      WHERE id = auth.uid() AND role = 'super_admin'
    )
  );

-- Users can only view their own sessions
CREATE POLICY "Users can view own sessions" ON admin_sessions
  FOR SELECT USING (admin_user_id = auth.uid());

-- Create function to update updated_at timestamp
CREATE OR REPLACE FUNCTION update_admin_users_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Create trigger for updated_at
CREATE TRIGGER admin_users_updated_at_trigger
BEFORE UPDATE ON admin_users
FOR EACH ROW
EXECUTE FUNCTION update_admin_users_updated_at();

-- Create function to log admin actions
CREATE OR REPLACE FUNCTION log_admin_action(
  p_admin_user_id UUID,
  p_action VARCHAR,
  p_entity_type VARCHAR DEFAULT NULL,
  p_entity_id VARCHAR DEFAULT NULL,
  p_old_values JSONB DEFAULT NULL,
  p_new_values JSONB DEFAULT NULL,
  p_ip_address INET DEFAULT NULL,
  p_user_agent TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_log_id UUID;
BEGIN
  INSERT INTO admin_audit_logs (
    admin_user_id,
    action,
    entity_type,
    entity_id,
    old_values,
    new_values,
    ip_address,
    user_agent
  ) VALUES (
    p_admin_user_id,
    p_action,
    p_entity_type,
    p_entity_id,
    p_old_values,
    p_new_values,
    p_ip_address,
    p_user_agent
  )
  RETURNING id INTO v_log_id;

  RETURN v_log_id;
END;
$$ LANGUAGE plpgsql;

-- Create function to record login attempt
CREATE OR REPLACE FUNCTION record_login_attempt(
  p_email VARCHAR,
  p_success BOOLEAN,
  p_error_message TEXT DEFAULT NULL,
  p_ip_address INET DEFAULT NULL,
  p_user_agent TEXT DEFAULT NULL
)
RETURNS UUID AS $$
DECLARE
  v_attempt_id UUID;
BEGIN
  INSERT INTO admin_login_attempts (
    email,
    success,
    error_message,
    ip_address,
    user_agent
  ) VALUES (
    LOWER(p_email),
    p_success,
    p_error_message,
    p_ip_address,
    p_user_agent
  )
  RETURNING id INTO v_attempt_id;

  RETURN v_attempt_id;
END;
$$ LANGUAGE plpgsql;

-- Create function to check recent login attempts (brute force detection)
CREATE OR REPLACE FUNCTION check_login_attempts(
  p_ip_address INET,
  p_minutes INT DEFAULT 15,
  p_max_attempts INT DEFAULT 5
)
RETURNS BOOLEAN AS $$
DECLARE
  v_attempt_count INT;
BEGIN
  SELECT COUNT(*) INTO v_attempt_count
  FROM admin_login_attempts
  WHERE ip_address = p_ip_address
    AND success = false
    AND created_at > now() - (p_minutes || ' minutes')::INTERVAL;

  RETURN v_attempt_count >= p_max_attempts;
END;
$$ LANGUAGE plpgsql;

-- Create function to update last login timestamp
CREATE OR REPLACE FUNCTION update_last_login(p_admin_user_id UUID)
RETURNS VOID AS $$
BEGIN
  UPDATE admin_users
  SET last_login_at = now()
  WHERE id = p_admin_user_id;
END;
$$ LANGUAGE plpgsql;

-- Create function to clean up expired sessions
CREATE OR REPLACE FUNCTION cleanup_expired_sessions()
RETURNS INT AS $$
DECLARE
  v_deleted INT;
BEGIN
  DELETE FROM admin_sessions
  WHERE expires_at < now();

  GET DIAGNOSTICS v_deleted = ROW_COUNT;
  RETURN v_deleted;
END;
$$ LANGUAGE plpgsql;

-- Create function to deactivate inactive users (optional)
CREATE OR REPLACE FUNCTION deactivate_inactive_users(p_days INT DEFAULT 90)
RETURNS INT AS $$
DECLARE
  v_updated INT;
BEGIN
  UPDATE admin_users
  SET is_active = false,
      updated_at = now()
  WHERE is_active = true
    AND (last_login_at IS NULL OR last_login_at < now() - (p_days || ' days')::INTERVAL)
    AND role != 'super_admin';

  GET DIAGNOSTICS v_updated = ROW_COUNT;
  RETURN v_updated;
END;
$$ LANGUAGE plpgsql;

