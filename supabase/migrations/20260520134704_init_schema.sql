DROP TYPE IF EXISTS payment_status;
CREATE TYPE payment_status AS ENUM ('pending', 'failed', 'completed');
CREATE TYPE admin_role AS ENUM ('super_admin', 'admin', 'moderator', 'editor');

CREATE TABLE IF NOT EXISTS public.customers (
  id text NOT NULL DEFAULT gen_random_uuid(),
  name character varying NOT NULL,
  email character varying NOT NULL,
  phone character varying,
  amount numeric NOT NULL,
  product_id integer[] NOT NULL DEFAULT '{}'::integer[],
  paid boolean NOT NULL DEFAULT false,
  created_at timestamp without time zone NOT NULL DEFAULT now(),
  CONSTRAINT customers_pkey PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS public.payments (
  id text NOT NULL DEFAULT gen_random_uuid(),
  customer_id text,
  amount numeric NOT NULL,
  payment_status payment_status NOT NULL DEFAULT 'pending'::payment_status,
  payment_date timestamp without time zone NOT NULL,
  transaction_id text NOT NULL UNIQUE,
  payment_method text NOT NULL,
  gateway_response text,
  CONSTRAINT payments_pkey PRIMARY KEY (id),
  CONSTRAINT payments_customer_id_customers_id_fk FOREIGN KEY (customer_id) REFERENCES public.customers(id)
);
CREATE TABLE IF NOT EXISTS public.products (
  id integer GENERATED BY DEFAULT AS IDENTITY,
  image_url text,
  name character varying NOT NULL,
  description text NOT NULL,
  features text,
  tag text,
  price integer NOT NULL,
  created_at timestamp without time zone NOT NULL DEFAULT now(),
  CONSTRAINT products_pkey PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS admin_users (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  email VARCHAR(255) UNIQUE NOT NULL,
  password_hash VARCHAR(255) NOT NULL,
  name VARCHAR(255),
  avatar_url VARCHAR(500),
  role admin_role NOT NULL DEFAULT 'admin',
  is_active BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT now(),
  last_login_at TIMESTAMP WITH TIME ZONE,
  phone VARCHAR(20),
  bio TEXT,
  department VARCHAR(100),
  created_by UUID REFERENCES admin_users(id) ON DELETE SET NULL,
  updated_by UUID REFERENCES admin_users(id) ON DELETE SET NULL,
  CONSTRAINT email_lowercase CHECK (email = LOWER(email)),
  CONSTRAINT valid_email CHECK (email ~ '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Z|a-z]{2,}$')
);

