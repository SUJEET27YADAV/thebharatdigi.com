-- Create view for active admin users
CREATE VIEW active_admin_users AS
SELECT
  id,
  email,
  name,
  role,
  avatar_url,
  created_at,
  last_login_at
FROM admin_users
WHERE is_active = true
ORDER BY created_at DESC;

-- Create view for admin statistics
CREATE VIEW admin_stats AS
SELECT
  COUNT(*) as total_admins,
  COUNT(*) FILTER (WHERE role = 'super_admin') as super_admins,
  COUNT(*) FILTER (WHERE role = 'admin') as admins,
  COUNT(*) FILTER (WHERE role = 'moderator') as moderators,
  COUNT(*) FILTER (WHERE role = 'editor') as editors,
  COUNT(*) FILTER (WHERE is_active = true) as active_admins,
  COUNT(*) FILTER (WHERE is_active = false) as inactive_admins,
  MAX(created_at) as latest_admin_created,
  COUNT(DISTINCT DATE(last_login_at)) as unique_login_days_this_month
FROM admin_users;
